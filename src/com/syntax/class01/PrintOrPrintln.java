package com.syntax.class01;

public class PrintOrPrintln {

	public static void main(String[] args) {
		/*
		 * Print--> prints statement and creates new line
		 * 
		 * Print--> just prints a statement & leaves cursor on same-line( no new line is added)
		 */
		
		System.out.println("This is print command"); // It print & leave the cursor there
		
		System.out.print("This is println command"); // It print & moves to the next
		
		System.out.println("Hello");
		System.out.print("Bye");
		
		// this is the end of my program
	
	
	
	
	}

}
