package com.syntax.class07;

public class ClassWorkDoWhileloop {

	public static void main(String[] args) {
		/*
		 * Print numbers from 30 to 70 using do while loop
		 * print odd numbers from 99 to 1
		 * 
		 * 
		 */

		int b= 30;
		
		do {
			
			System.out.print(b+" ");
			
			b++;
		}while(b<=70);
		
		System.out.println("");
	System.out.println("------Practice2-------");
	
	int i=99;
	
	do {
		System.out.print(i+" ");
	i--;
	}while(i>=1);
	
	}
		
	
}

