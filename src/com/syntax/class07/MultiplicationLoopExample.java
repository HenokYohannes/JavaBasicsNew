package com.syntax.class07;

public class MultiplicationLoopExample {

	public static void main(String[] args) {
		

int num=9;
	for(int i=1; i<=10; i++) {
		System.out.println(num+" X "+i +" = "+num*i);
	}
	System.out.println("-------------");
	for(int i=1; i<=20;i+=2) {
		System.out.print(i+" ");
	}
	
	
	}

}
